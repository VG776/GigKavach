# ─── Lifespan (Startup + Shutdown) ───────────────────────────────────────────
@asynccontextmanager
async def lifespan(app: FastAPI):
    """
    Modern FastAPI lifespan handler — replaces deprecated @app.on_event decorators.
    Code before `yield` runs on startup; code after runs on shutdown.
    """
    # ── STARTUP ──────────────────────────────────────────────────────────────
    logger.info("=" * 60)
    logger.info("  GigKavach API starting up...")
    logger.info(f"  Environment: {settings.APP_ENV}")
    logger.info("=" * 60)

    # ── STRICT STARTUP VALIDATION ─────────────────────────────────────────────
    # Validate that all ESSENTIAL credentials are set before proceeding.
    # If validation fails, CRASH immediately (don't just warn).
    # This prevents confusing 500 errors on first database query.
    
    from utils.error_response import ConfigurationError
    
    missing_essential = []
    if not (settings.SUPABASE_URL or "").strip():
        missing_essential.append("SUPABASE_URL")
    if not (settings.SUPABASE_SERVICE_ROLE_KEY or "").strip():
        missing_essential.append("SUPABASE_SERVICE_ROLE_KEY")
    
    if missing_essential:
        error_msg = f"❌ STARTUP FAILED: Missing essential environment variables: {', '.join(missing_essential)}"
        logger.critical(error_msg)
        # Stop the application from starting
        raise ConfigurationError(
            message=error_msg,
            missing_vars=missing_essential
        )
    
    # Warn about optional credentials (fallback available)
    if not (settings.TOMORROW_IO_API_KEY or "").strip():
        logger.warning("⚠️  TOMORROW_IO_API_KEY not set — DCI weather component will use mock data")
    if not (settings.AQICN_API_TOKEN or "").strip():
        logger.warning("⚠️  AQICN_API_TOKEN not set — AQI component will use mock data")
    
    logger.info("✅ All essential credentials validated successfully")

    # ── Start APScheduler for DCI Engine ─────────────────────────────────────
    # The DCI engine polls weather/AQI/social APIs every 5 minutes and
    # updates the DCI score for all active zones.
    # We import lazily here so this file doesn't crash if DCI engine isn't built yet.
    try:
        from apscheduler.schedulers.asyncio import AsyncIOScheduler

        scheduler = AsyncIOScheduler()

        # (Varshit): Uncommented and wired to cron poller
        from cron.dci_poller import run_dci_cycle
        from cron.settlement_service import run_daily_settlement

        scheduler.add_job(
            run_dci_cycle,
            "interval",
            seconds=settings.DCI_POLL_INTERVAL_SECONDS,
            id="dci_engine",
            name="DCI Engine Poll",
        )

        # ── Daily Settlement Execution 11:55 PM (Image #5 Requirement) ───────────
        scheduler.add_job(
            run_daily_settlement,
            "cron",
            hour=23,
            minute=55,
            id="daily_settlement",
            name="End of Day Payout Settlement",
        )

        # Task 11: Replace dummy heartbeat with the real claims pipeline job.
        # The claims pipeline runs every 5 min alongside the DCI engine.
        from cron.claims_trigger import run_claims_pipeline
        scheduler.add_job(
            run_claims_pipeline,
            "interval",
            seconds=settings.DCI_POLL_INTERVAL_SECONDS,  # same 5-min cadence
            id="claims_pipeline",
            name="Claims Processing Pipeline",
        )

        # ── Self Keep-Alive (Prevent Render Spin-Down) ──────────────────────
        # Ping the health endpoint every 5 min to keep backend warm on Render free tier
        from cron.keep_alive import run_keep_alive
        scheduler.add_job(
            run_keep_alive,
            "interval",
            seconds=settings.DCI_POLL_INTERVAL_SECONDS,  # 5-min cadence, synced with DCI
            id="keep_alive",
            name="Backend Keep-Alive (Render Free Tier)",
        )

        scheduler.start()
        app.state.scheduler = scheduler
        logger.info(f"APScheduler started | DCI poll every {settings.DCI_POLL_INTERVAL_SECONDS}s")

    except Exception as e:
        logger.error(f"Failed to start APScheduler: {e}")

    logger.info("GigKavach API ready ✅")

    yield  # ← Application runs here

    # ── SHUTDOWN ─────────────────────────────────────────────────────────────
    logger.info("GigKavach API shutting down...")
    if hasattr(app.state, "scheduler"):
        app.state.scheduler.shutdown(wait=False)
        logger.info("APScheduler stopped")



